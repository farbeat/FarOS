static int32_t ruxin_lcd_display_init(combo_dev_cfg_t *attr)
{
    int32_t ret;
    uint32_t i;
    test_cmd_sequ *cmd_desc = g_ruxin_screen_init_on;
    uint32_t cmd_cnt = lcd_array_size(g_ruxin_screen_init_on);

    for (i = 0; i < cmd_cnt; i++) {
        if (cmd_desc[i].data[0] == 0x3a) {
            if (attr->out_format == OUT_FORMAT_RGB_16BIT) {
                cmd_desc[i].data[1] = 0x75; /* rgb16 */
            } else if (attr->out_format == OUT_FORMAT_RGB_18BIT) {
                cmd_desc[i].data[1] = 0x76; /* rgb18 */
            } else {
                cmd_desc[i].data[1] = 0x77; /* rgb24, rgb18 loosely */
            }
        }
        if (cmd_desc[i].dcs_flag != 0) {
            ret = test_dcs_cmd_send(&(cmd_desc[i]));
            if (ret != EXT_ERR_SUCCESS) {
                return ret;
            }
        } else {
            ret = test_dcs_cmd_send(&(cmd_desc[i]));
            if (ret != EXT_ERR_SUCCESS) {
                return ret;
            }
        }
    }
    return ret;
}

test_drv_api g_ruxin_api = {
    .lcd_init = ruxin_lcd_display_init,
};

