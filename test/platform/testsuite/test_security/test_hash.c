static char get_high_hex(uint8_t buffer_byte)
{
    char r = 0;
    buffer_byte >>= 4; // 右移4bit,取高4bit

    if (buffer_byte <= 9) { // 小于等于9,'0'~'9'表示
        r = buffer_byte + '0';
    } else {
        r = buffer_byte - 10 + 'A'; // 大于等于10,'A'~'F'表示
    }
    return r;
}

static char get_low_hex(uint8_t buffer_byte)
{
    char r = 0;
    buffer_byte &= 0x0f;

    if (buffer_byte <= 9) { // 小于等于9,'0'~'9'表示
        r = buffer_byte + '0';
    } else {
        r = buffer_byte - 10 + 'A'; // 大于等于10,'A'~'F'表示
    }
    return r;
}

static void dump_buf(const char* buffer_name, const uint8_t* buffer, const uint32_t len)
{
    uint32_t i;
    uint8_t dump_byte[DUMP_BYTES_ONCE_LEN * DUMP_BYTES_TIMES + 1] = {0};
    uint8_t *ptr = dump_byte;

    if (buffer_name == NULL || buffer == NULL) {
        PRINT("the buffer is null\r\n");
        return;
    }

    PRINT("name:%s, addr:0x%x, len:0x%x\r\n", buffer_name, buffer, len);

    for (i = 0; i < len; i++) {
        *(ptr++) = get_high_hex(buffer[i]);
        *(ptr++) = get_low_hex(buffer[i]);
        *(ptr++) = ' ';
        if ((ptr - dump_byte) == (sizeof(dump_byte) - 1)) {
            *ptr = '\0';
            ptr = dump_byte;
            PRINT("%s\r\n", dump_byte);
        }
    }

    if (ptr != dump_byte) {
        *ptr = '\0';
        PRINT("%s\r\n", dump_byte);
    }
}

static int hash_sha256_basic_test(int argc, char *argv[])
{
    UNUSED(argc);
    UNUSED(argv);
    int ret;
    uint32_t handle;
    uint32_t block_size = 64;
    uapi_drv_cipher_hash_attr_t hash_attr;
    hash_attr.hash_type = UAPI_DRV_CIPHER_HASH_TYPE_SHA256;
    uapi_drv_cipher_buf_attr_t buf;
    buf.phys_addr = (uintptr_t)g_test_buf1;
    buf.buf_sec = UAPI_DRV_CIPHER_BUF_NONSECURE;
    static const uint8_t expect_result[] = {
        0xD9, 0x5D, 0x7A, 0xD4, 0x4B, 0xAF, 0x70, 0xE1,
        0xFB, 0x52, 0xA5, 0x1A, 0xCB, 0xFF, 0xD6, 0xBB,
        0x66, 0xA5, 0xD8, 0x70, 0xB9, 0x3A, 0xA6, 0x46,
        0x80, 0xA0, 0xA2, 0xDD, 0x2B, 0x32, 0xA2, 0x81
    };

    uint32_t result_len = sizeof(g_hash_result);
    int32_t update_len = block_size;

    ret = uapi_drv_cipher_hash_start(&handle, &hash_attr);
    if (ret != ERRCODE_SUCC) {
        return TEST_SUITE_TEST_FAILED;
    }

    // first update: 64 bytes
    ret = uapi_drv_cipher_hash_update(handle, &buf, update_len);
    if (ret != ERRCODE_SUCC) {
        return TEST_SUITE_TEST_FAILED;
    }
    // second update: 64 + 65 bytes
    buf.phys_addr = (uintptr_t)g_test_buf2;
    update_len = block_size * 2 + 1; // 2表示两个block
    ret = uapi_drv_cipher_hash_update(handle, &buf, update_len);
    if (ret != ERRCODE_SUCC) {
        return TEST_SUITE_TEST_FAILED;
    }

    ret = uapi_drv_cipher_hash_finish(handle, g_hash_result, &result_len);
    if (ret != ERRCODE_SUCC) {
        return TEST_SUITE_TEST_FAILED;
    }
    if (result_len != sizeof(expect_result)) {
        return TEST_SUITE_TEST_FAILED;
    }

    dump_buf("input data 1", g_test_buf1, block_size);
    dump_buf("input data 2", g_test_buf2, block_size * 2 + 1);
    dump_buf("expect_result", expect_result, result_len);
    dump_buf("g_hash_result", g_hash_result, result_len);
    if (memcmp(expect_result, g_hash_result, result_len) != 0) {
        return TEST_SUITE_TEST_FAILED;
    }

    return TEST_SUITE_OK;
}

