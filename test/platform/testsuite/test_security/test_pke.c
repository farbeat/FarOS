static int rsa_exp_mod_test(int argc, char *argv[])
{
    UNUSED(argc);
    uint32_t plain_text_addr = (uint32_t)strtol(argv[0], NULL, 0);
    uint32_t test_result[RSA_4096_RESULT_LENGTH] = { 0x00 };

    if (memcpy_s((uint8_t *)(uintptr_t)plain_text_addr, RSA_4096_RESULT_LENGTH,
                  g_plain_text, RSA_4096_RESULT_LENGTH) != EOK) {
        PRINT("memcopy_s error");
        return TEST_SUITE_TEST_FAILED;
    }

    PRINT("****************************rsa exp mod********************************\n");
    dump_buf("plain text", (uint8_t*)(uintptr_t)g_plain_text, RSA_4096_RESULT_LENGTH);
    dump_buf("rsa_key", (uint8_t*)(uintptr_t)g_key, RSA_4096_RESULT_LENGTH * 8);

    if (!rsa_exp_mod((uint32_t)(uintptr_t)g_key, (uint8_t*)g_plain_text, (uint8_t*)(uintptr_t)test_result)) {
        return TEST_SUITE_TEST_FAILED;
    }

    dump_buf("real result", (uint8_t*)(uintptr_t)test_result, RSA_4096_RESULT_LENGTH);
    dump_buf("expect result", (uint8_t*)(uintptr_t)g_expected_result, RSA_4096_RESULT_LENGTH);
    for (uint32_t i = 0; i < RSA_4096_RESULT_LENGTH; i++) {
        if (test_result[i] != g_expected_result[i]) {
            PRINT("Test result error!\n");
            return TEST_SUITE_TEST_FAILED;
        }
    }
    return TEST_SUITE_OK;
}

